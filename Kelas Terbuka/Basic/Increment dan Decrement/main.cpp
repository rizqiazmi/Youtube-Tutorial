#include <iostream>
using namespace std;

int main(){
    // incerment dan decrement
    // preincerment dan postincerment

    int a = 5;
    int b = 5;

    // postincerment
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl << endl;

    // preincerement
    cout << b << endl;
    cout << ++b << endl;
    cout << b << endl;

    cin.get();
    return 0;
}
